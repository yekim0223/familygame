# ── 헬퍼 ────────────────────────────────────────────────────
def sf(run, name="맑은 고딕", size=11, bold=False, color=None, italic=False):
    run.font.name=name; run.font.size=Pt(size)
    run.font.bold=bold; run.font.italic=italic
    if color: run.font.color.rgb=RGBColor(*color)
    rPr=run._r.get_or_add_rPr()
    rf=OxmlElement('w:rFonts'); rf.set(qn('w:eastAsia'),name); rPr.insert(0,rf)

def sp(p,before=0,after=0,line=None):
    pPr=p._p.get_or_add_pPr(); el=OxmlElement('w:spacing')
    el.set(qn('w:before'),str(before)); el.set(qn('w:after'),str(after))
    if line: el.set(qn('w:line'),str(line)); el.set(qn('w:lineRule'),'auto')
    pPr.append(el)

def ind(p,left=0,hanging=0):
    pPr=p._p.get_or_add_pPr(); el=OxmlElement('w:ind')
    el.set(qn('w:left'),str(left))
    if hanging: el.set(qn('w:hanging'),str(hanging))
    pPr.append(el)

def shd(cell,fill):
    tc=cell._tc; tcPr=tc.get_or_add_tcPr()
    el=OxmlElement('w:shd'); el.set(qn('w:val'),'clear')
    el.set(qn('w:color'),'auto'); el.set(qn('w:fill'),fill); tcPr.append(el)

def tcm(cell,v=90,h=110):
    tc=cell._tc; tcPr=tc.get_or_add_tcPr(); m=OxmlElement('w:tcMar')
    for sd in ['top','bottom']:
        e=OxmlElement(f'w:{sd}'); e.set(qn('w:w'),str(v)); e.set(qn('w:type'),'dxa'); m.append(e)
    for sd in ['left','right']:
        e=OxmlElement(f'w:{sd}'); e.set(qn('w:w'),str(h)); e.set(qn('w:type'),'dxa'); m.append(e)
    tcPr.append(m)

def bdr(cell,color='BBBBBB'):
    tc=cell._tc; tcPr=tc.get_or_add_tcPr(); tb=OxmlElement('w:tcBorders')
    for sd in ['top','left','bottom','right']:
        b=OxmlElement(f'w:{sd}'); b.set(qn('w:val'),'single')
        b.set(qn('w:sz'),'4'); b.set(qn('w:space'),'0'); b.set(qn('w:color'),color); tb.append(b)
    tcPr.append(tb)

def bot_line(p,color='7B5EA7',sz=8):
    pPr=p._p.get_or_add_pPr(); pb2=OxmlElement('w:pBdr'); b=OxmlElement('w:bottom')
    b.set(qn('w:val'),'single'); b.set(qn('w:sz'),str(sz))
    b.set(qn('w:space'),'1'); b.set(qn('w:color'),color); pb2.append(b); pPr.append(pb2)

def h1(text,c=(90,50,140)):
    p=doc.add_paragraph(); sp(p,before=260,after=120); bot_line(p,'%02X%02X%02X'%tuple(c),8)
    r=p.add_run(text); sf(r,size=17,bold=True,color=c); return p

def h2(text,c=(120,70,170)):
    p=doc.add_paragraph(); sp(p,before=180,after=80)
    r=p.add_run('▶  '+text); sf(r,size=13,bold=True,color=c); return p

def h3(text,c=(80,80,80)):
    p=doc.add_paragraph(); sp(p,before=120,after=60)
    r=p.add_run('◆  '+text); sf(r,size=11,bold=True,color=c); return p

def body(text):
    p=doc.add_paragraph(); sp(p,before=40,after=40)
    r=p.add_run(text); sf(r,size=10.5); return p

def bl(text,lv=1,icon='•'):
    p=doc.add_paragraph(); sp(p,before=26,after=26); ind(p,left=lv*360,hanging=200)
    r=p.add_run(f'{icon}  {text}'); sf(r,size=10.5); return p

def note(title,lines,bg='F5EFFE',bc=(120,70,170)):
    tbl2=doc.add_table(rows=1,cols=1); tbl2.alignment=WD_TABLE_ALIGNMENT.CENTER
    cell=tbl2.rows[0].cells[0]; shd(cell,bg); tcm(cell,v=120,h=160)
    tc=cell._tc; tcPr=tc.get_or_add_tcPr(); tb2=OxmlElement('w:tcBorders')
    for sd in ['top','left','bottom','right']:
        b=OxmlElement(f'w:{sd}'); b.set(qn('w:val'),'single'); b.set(qn('w:sz'),'10')
        b.set(qn('w:space'),'0'); b.set(qn('w:color'),'%02X%02X%02X'%tuple(bc)); tb2.append(b)
    tcPr.append(tb2)
    pt=cell.paragraphs[0]; sp(pt,before=40,after=40)
    rt=pt.add_run(f'  {title}'); sf(rt,size=11,bold=True,color=bc)
    for line in lines:
        p2=cell.add_paragraph(); sp(p2,before=18,after=18); ind(p2,left=120)
        r2=p2.add_run(line); sf(r2,size=10)
    doc.add_paragraph()

def tbl(headers,rows,widths=None):
    t=doc.add_table(rows=1+len(rows),cols=len(headers)); t.alignment=WD_TABLE_ALIGNMENT.CENTER
    hr=t.rows[0]
    for i,h in enumerate(headers):
        c=hr.cells[i]; shd(c,'5B2D8E'); bdr(c,'3D1A6B'); tcm(c)
        p=c.paragraphs[0]; p.alignment=WD_ALIGN_PARAGRAPH.CENTER
        r=p.add_run(h); sf(r,size=10,bold=True,color=(255,240,255))
    for ri,row in enumerate(rows):
        fill='FAF0FF' if ri%2==0 else 'FFFFFF'; tr=t.rows[ri+1]
        for ci,val in enumerate(row):
            c=tr.cells[ci]; shd(c,fill); bdr(c,'D4AAFF'); tcm(c)
            r=c.paragraphs[0].add_run(str(val)); sf(r,size=10)
    if widths:
        for row in t.rows:
            for ci,c in enumerate(row.cells):
                if ci<len(widths): c.width=Cm(widths[ci])
    doc.add_paragraph()

def pb():
    p=doc.add_paragraph(); r=p.add_run()
    br=OxmlElement('w:br'); br.set(qn('w:type'),'page'); r._r.append(br)

def div(color='9B59B6'):
    p=doc.add_paragraph(); sp(p,before=60,after=60)
    pPr=p._p.get_or_add_pPr(); pb2=OxmlElement('w:pBdr'); b=OxmlElement('w:bottom')
    b.set(qn('w:val'),'single'); b.set(qn('w:sz'),'4'); b.set(qn('w:space'),'1')
    b.set(qn('w:color'),color); pb2.append(b); pPr.append(pb2)

