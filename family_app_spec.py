# ─── 스타일 헬퍼 ───────────────────────────────────────────────
def set_font(run, name="맑은 고딕", size=11, bold=False, color=None, italic=False):
    run.font.name = name
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    if color:
        run.font.color.rgb = RGBColor(*color)
    r = run._r
    rPr = r.get_or_add_rPr()
    rFonts = OxmlElement('w:rFonts')
    rFonts.set(qn('w:eastAsia'), name)
    rPr.insert(0, rFonts)

def set_para_spacing(para, before=0, after=0, line=None):
    pPr = para._p.get_or_add_pPr()
    spacing = OxmlElement('w:spacing')
    spacing.set(qn('w:before'), str(before))
    spacing.set(qn('w:after'), str(after))
    if line:
        spacing.set(qn('w:line'), str(line))
        spacing.set(qn('w:lineRule'), 'auto')
    pPr.append(spacing)

def set_shading(cell, fill_hex):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), fill_hex)
    tcPr.append(shd)

def set_borders(cell):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders = OxmlElement('w:tcBorders')
    for side in ['top','left','bottom','right']:
        bd = OxmlElement(f'w:{side}')
        bd.set(qn('w:val'), 'single')
        bd.set(qn('w:sz'), '4')
        bd.set(qn('w:space'), '0')
        bd.set(qn('w:color'), 'AAAAAA')
        tcBorders.append(bd)
    tcPr.append(tcBorders)

def h1(text, color=(30, 90, 160)):
    p = doc.add_paragraph()
    set_para_spacing(p, before=240, after=120)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    # 배경선
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '6')
    bottom.set(qn('w:space'), '1')
    bottom.set(qn('w:color'), '%02X%02X%02X' % tuple(color))
    pBdr.append(bottom)
    pPr.append(pBdr)
    r = p.add_run(text)
    set_font(r, size=16, bold=True, color=color)
    return p

def h2(text, color=(50, 130, 50)):
    p = doc.add_paragraph()
    set_para_spacing(p, before=180, after=80)
    r = p.add_run('▶ ' + text)
    set_font(r, size=13, bold=True, color=color)
    return p

def h3(text, color=(80, 80, 80)):
    p = doc.add_paragraph()
    set_para_spacing(p, before=120, after=60)
    r = p.add_run('◆ ' + text)
    set_font(r, size=11, bold=True, color=color)
    return p

def body(text, indent=0):
    p = doc.add_paragraph()
    set_para_spacing(p, before=40, after=40)
    if indent:
        pPr = p._p.get_or_add_pPr()
        ind = OxmlElement('w:ind')
        ind.set(qn('w:left'), str(indent * 360))
        pPr.append(ind)
    r = p.add_run(text)
    set_font(r, size=10.5)
    return p

def bullet(text, level=1, icon='•'):
    p = doc.add_paragraph()
    set_para_spacing(p, before=30, after=30)
    pPr = p._p.get_or_add_pPr()
    ind = OxmlElement('w:ind')
    ind.set(qn('w:left'), str(level * 360))
    ind.set(qn('w:hanging'), '180')
    pPr.append(ind)
    r = p.add_run(f'{icon}  {text}')
    set_font(r, size=10.5)
    return p

def info_box(title, content_lines, bg='EBF5FB', border_color=(52,152,219)):
    tbl = doc.add_table(rows=1, cols=1)
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    cell = tbl.rows[0].cells[0]
    set_shading(cell, bg)
    # 테두리
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders = OxmlElement('w:tcBorders')
    for side in ['top','left','bottom','right']:
        bd = OxmlElement(f'w:{side}')
        bd.set(qn('w:val'), 'single')
        bd.set(qn('w:sz'), '8')
        bd.set(qn('w:space'), '0')
        bd.set(qn('w:color'), '%02X%02X%02X' % tuple(border_color))
        tcBorders.append(bd)
    tcPr.append(tcBorders)
    tcMar = OxmlElement('w:tcMar')
    for side in ['top','left','bottom','right']:
        m = OxmlElement(f'w:{side}')
        m.set(qn('w:w'), '120')
        m.set(qn('w:type'), 'dxa')
        tcMar.append(m)
    tcPr.append(tcMar)
    p_title = cell.paragraphs[0]
    set_para_spacing(p_title, before=40, after=40)
    rt = p_title.add_run(f'💡 {title}')
    set_font(rt, size=10.5, bold=True, color=border_color)
    for line in content_lines:
        p2 = cell.add_paragraph()
        set_para_spacing(p2, before=20, after=20)
        r2 = p2.add_run(f'   {line}')
        set_font(r2, size=10)
    doc.add_paragraph()
    return tbl

def add_table(headers, rows, col_widths=None):
    tbl = doc.add_table(rows=1+len(rows), cols=len(headers))
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    # 헤더
    hrow = tbl.rows[0]
    for i, h in enumerate(headers):
        cell = hrow.cells[i]
        set_shading(cell, '2E5B9A')
        set_borders(cell)
        tc = cell._tc
        tcPr = tc.get_or_add_tcPr()
        tcMar = OxmlElement('w:tcMar')
        for side in ['top','left','bottom','right']:
            m = OxmlElement(f'w:{side}')
            m.set(qn('w:w'), '100')
            m.set(qn('w:type'), 'dxa')
            tcMar.append(m)
        tcPr.append(tcMar)
        p = cell.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.add_run(h)
        set_font(r, size=10, bold=True, color=(255,255,255))
    # 데이터
    for ri, row_data in enumerate(rows):
        fill = 'F5F5F5' if ri%2==0 else 'FFFFFF'
        row = tbl.rows[ri+1]
        for ci, val in enumerate(row_data):
            cell = row.cells[ci]
            set_shading(cell, fill)
            set_borders(cell)
            tc = cell._tc
            tcPr = tc.get_or_add_tcPr()
            tcMar = OxmlElement('w:tcMar')
            for side in ['top','left','bottom','right']:
                m = OxmlElement(f'w:{side}')
                m.set(qn('w:w'), '100')
                m.set(qn('w:type'), 'dxa')
                tcMar.append(m)
            tcPr.append(tcMar)
            p = cell.paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            r = p.add_run(str(val))
            set_font(r, size=10)
    if col_widths:
        for ri, row in enumerate(tbl.rows):
            for ci, cell in enumerate(row.cells):
                cell.width = Cm(col_widths[ci])
    doc.add_paragraph()
    return tbl

def page_break():
    p = doc.add_paragraph()
    r = p.add_run()
    br = OxmlElement('w:br')
    br.set(qn('w:type'), 'page')
    r._r.append(br)

def divider():
    p = doc.add_paragraph()
    set_para_spacing(p, before=60, after=60)
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '4')
    bottom.set(qn('w:space'), '1')
    bottom.set(qn('w:color'), 'CCCCCC')
    pBdr.append(bottom)
    pPr.append(pBdr)
    return p

