def body(text):
    p = doc.add_paragraph()
    spacing(p, before=40, after=40)
    r = p.add_run(text)
    set_font(r, size=10.5)
    return p

def bullet(text, lv=1, icon='•'):
    p = doc.add_paragraph()
    spacing(p, before=28, after=28)
    indent(p, left=lv*360, hanging=200)
    r = p.add_run(f'{icon}  {text}')
    set_font(r, size=10.5)
    return p

def note(title, lines, bg='EBF5FB', bc=(52,130,200)):
    tbl = doc.add_table(rows=1, cols=1)
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    cell = tbl.rows[0].cells[0]
    shading(cell, bg)
    cell_margins(cell, v=120, h=160)
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders2 = OxmlElement('w:tcBorders')
    for side in ['top','left','bottom','right']:
        bd = OxmlElement(f'w:{side}')
        bd.set(qn('w:val'), 'single')
        bd.set(qn('w:sz'), '10')
        bd.set(qn('w:space'), '0')
        bd.set(qn('w:color'), '%02X%02X%02X'%tuple(bc))
        tcBorders2.append(bd)
    tcPr.append(tcBorders2)
    pt = cell.paragraphs[0]
    spacing(pt, before=40, after=40)
    rt = pt.add_run(f'  {title}')
    set_font(rt, size=11, bold=True, color=bc)
    for line in lines:
        p2 = cell.add_paragraph()
        spacing(p2, before=20, after=20)
        indent(p2, left=120)
        r2 = p2.add_run(line)
        set_font(r2, size=10)
    doc.add_paragraph()

def table(headers, rows, widths=None):
    tbl = doc.add_table(rows=1+len(rows), cols=len(headers))
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    # header row
    hr = tbl.rows[0]
    for i, h in enumerate(headers):
        c = hr.cells[i]
        shading(c, '2A5F2A')
        borders(c, color='1A4F1A', sz='4')
        cell_margins(c)
        p = c.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.add_run(h)
        set_font(r, size=10, bold=True, color=(255,255,220))
    # data rows
    for ri, row in enumerate(rows):
        fill = 'F2F8F2' if ri%2==0 else 'FFFFFF'
        tr = tbl.rows[ri+1]
        for ci, val in enumerate(row):
            c = tr.cells[ci]
            shading(c, fill)
            borders(c)
            cell_margins(c)
            p = c.paragraphs[0]
            r = p.add_run(str(val))
            set_font(r, size=10)
    if widths:
        for row in tbl.rows:
            for ci, c in enumerate(row.cells):
                if ci < len(widths):
                    c.width = Cm(widths[ci])
    doc.add_paragraph()

def page_break():
    p = doc.add_paragraph()
    r = p.add_run()
    br = OxmlElement('w:br')
    br.set(qn('w:type'), 'page')
    r._r.append(br)

def divider(color='AAAAAA'):
    p = doc.add_paragraph()
    spacing(p, before=60, after=60)
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    b = OxmlElement('w:bottom')
    b.set(qn('w:val'), 'single')
    b.set(qn('w:sz'), '4')
    b.set(qn('w:space'), '1')
    b.set(qn('w:color'), color)
    pBdr.append(b)
    pPr.append(pBdr)

def center(text, size=11, bold=False, color=(80,80,80)):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    spacing(p, before=60, after=60)
    r = p.add_run(text)
    set_font(r, size=size, bold=bold, color=color)
    return p

