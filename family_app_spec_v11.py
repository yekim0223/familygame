def rf(name="맑은 고딕"):
    rPr = OxmlElement('w:rPr')
    rFonts = OxmlElement('w:rFonts')
    rFonts.set(qn('w:eastAsia'), name)
    rPr.insert(0, rFonts)
    return rPr

def set_font(run, name="맑은 고딕", size=11, bold=False, color=None, italic=False):
    run.font.name = name
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    if color:
        run.font.color.rgb = RGBColor(*color)
    rPr = run._r.get_or_add_rPr()
    rFonts = OxmlElement('w:rFonts')
    rFonts.set(qn('w:eastAsia'), name)
    rPr.insert(0, rFonts)

def spacing(para, before=0, after=0, line=None):
    pPr = para._p.get_or_add_pPr()
    sp = OxmlElement('w:spacing')
    sp.set(qn('w:before'), str(before))
    sp.set(qn('w:after'),  str(after))
    if line:
        sp.set(qn('w:line'),     str(line))
        sp.set(qn('w:lineRule'), 'auto')
    pPr.append(sp)

def indent(para, left=0, hanging=0):
    pPr = para._p.get_or_add_pPr()
    ind = OxmlElement('w:ind')
    ind.set(qn('w:left'), str(left))
    if hanging:
        ind.set(qn('w:hanging'), str(hanging))
    pPr.append(ind)

def shading(cell, fill):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'),   'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'),  fill)
    tcPr.append(shd)

def cell_margins(cell, v=100, h=120):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcMar = OxmlElement('w:tcMar')
    for side in ['top','bottom']:
        m = OxmlElement(f'w:{side}')
        m.set(qn('w:w'), str(v)); m.set(qn('w:type'), 'dxa')
        tcMar.append(m)
    for side in ['left','right']:
        m = OxmlElement(f'w:{side}')
        m.set(qn('w:w'), str(h)); m.set(qn('w:type'), 'dxa')
        tcMar.append(m)
    tcPr.append(tcMar)

def borders(cell, color='BBBBBB', sz='4'):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders = OxmlElement('w:tcBorders')
    for side in ['top','left','bottom','right']:
        bd = OxmlElement(f'w:{side}')
        bd.set(qn('w:val'), 'single')
        bd.set(qn('w:sz'), sz)
        bd.set(qn('w:space'), '0')
        bd.set(qn('w:color'), color)
        tcBorders.append(bd)
    tcPr.append(tcBorders)

def bottom_border(para, color='3D7A3D', sz=8):
    pPr = para._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    b = OxmlElement('w:bottom')
    b.set(qn('w:val'), 'single')
    b.set(qn('w:sz'), str(sz))
    b.set(qn('w:space'), '1')
    b.set(qn('w:color'), color)
    pBdr.append(b)
    pPr.append(pBdr)

# ─── 단락 유형 ────────────────────────────────────────────────
def h1(text, color=(30,100,30)):
    p = doc.add_paragraph()
    spacing(p, before=260, after=120)
    bottom_border(p, color='%02X%02X%02X'%tuple(color), sz=8)
    r = p.add_run(text)
    set_font(r, size=17, bold=True, color=color)
    return p

